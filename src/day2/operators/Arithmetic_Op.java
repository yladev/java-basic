package day2.operators;

public class Arithmetic_Op {
    public static void main(String[] args) {
        int aa = 10;
        int oo = 10;
        int ma = 10;
        System.out.println(aa + oo + ma);

        int kk = 19;
        int jj = 1;
        System.out.println(kk - jj);

        int ji = 13;
        int kl = 2;
        System.out.println(ji * kl);

        int lo = 9;
        int ll = 2;
        System.out.println(lo % ll);


    }
}
