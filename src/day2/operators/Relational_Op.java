package day2.operators;

public class Relational_Op {
    public static void main(String[] args) {
        int num1=10,num2=11;
        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1==num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1!=num2);
    }
}
