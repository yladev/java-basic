package day2.Operators;

public class Unary_Op {
    public static void main(String[] args) {
        int a = 10;
        a--; //decrease//
        System.out.println(a);
        a++;
        System.out.println(a);

        --a; //prefix//
        System.out.println(a);
        ++a; //postfix//
        System.out.println(a);
    }
}
