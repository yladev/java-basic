package day3.operators;

public class Relational_Operator {
    public static void main(String[] args) {
        //  < > <= >= == != InstanceOf

        int num1 = 15;
        int num2 = 14;
        System.out.println(num1 > num2);
        System.out.println(num1 < num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
    }
}
