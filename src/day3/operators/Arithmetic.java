package day3.operators;

public class Arithmetic {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 3;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
    }
}
