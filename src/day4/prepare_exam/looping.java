package day4.prepare_exam;

public class looping {
    public static void main(String[] args) {
        int i;
        for (i = 9; i >= 1; i --) {
            System.out.println(i);
        }

        int num1;
        for (num1 = 1; num1 >= 9; num1++) {
            System.out.println(num1);
        }
    }
}